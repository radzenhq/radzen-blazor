namespace Radzen;

/// <summary>
/// Menu Item display style enum
/// </summary>
public enum MenuItemDisplayStyle
{
    /// <summary>
    /// Show text only
    /// </summary>
    Text,

    /// <summary>
    /// Show only icons
    /// </summary>
    Icon,

    /// <summary>
    /// Both icons and text are displayed
    /// </summary>
    IconAndText
}

