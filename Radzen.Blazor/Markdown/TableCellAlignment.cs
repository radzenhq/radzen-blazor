namespace Radzen.Blazor.Markdown;

/// <summary>
/// The alignment of a table cell.
/// </summary>
public enum TableCellAlignment
{
    /// <summary>
    /// No alignment specified. Default alignment is left.
    /// </summary>
    None,

    /// <summary>
    /// Left alignment.
    /// </summary>
    Left,

    /// <summary>
    /// Center alignment.
    /// </summary>
    Center,

    /// <summary>
    /// Right alignment.
    /// </summary>
    Right
}

