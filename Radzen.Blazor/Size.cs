namespace Radzen;

/// <summary>
/// Specifies the size of a component.
/// </summary>
public enum InputSize
{
    /// <summary>
    /// An extra small size.
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// A small size.
    /// </summary>
    Small,

    /// <summary>
    /// A medium size. This is the default.
    /// </summary>
    Medium,

    /// <summary>
    /// A large size.
    /// </summary>
    Large
}
