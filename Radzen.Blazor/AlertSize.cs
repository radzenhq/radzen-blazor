namespace Radzen;

/// <summary>
/// Specifies the size of a <see cref="Radzen.Blazor.RadzenAlert" />.
/// </summary>
public enum AlertSize
{
    /// <summary>
    /// The smallest alert.
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// A alert smaller than the default.
    /// </summary>
    Small,

    /// <summary>
    /// The default size of an alert.
    /// </summary>
    Medium,

    /// <summary>
    /// An alert larger than the default.
    /// </summary>
    Large
}

