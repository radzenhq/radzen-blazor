namespace Radzen;

/// <summary>
/// Specifies the size of a <see cref="Radzen.Blazor.RadzenButton" />.
/// </summary>
public enum ButtonSize
{
    /// <summary>
    /// The default size of a button.
    /// </summary>
    Medium,

    /// <summary>
    /// A button larger than the default.
    /// </summary>
    Large,

    /// <summary>
    /// A button smaller than the default.
    /// </summary>
    Small,

    /// <summary>
    /// The smallest button.
    /// </summary>
    ExtraSmall
}

