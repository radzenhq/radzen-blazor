namespace Radzen;

/// <summary>
/// Specifies the size of a point in <see cref="Radzen.Blazor.RadzenTimelineItem" />.
/// </summary>
public enum PointSize
{
    /// <summary>
    /// The smallest.
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Smaller than the default.
    /// </summary>
    Small,

    /// <summary>
    /// The default size.
    /// </summary>
    Medium,

    /// <summary>
    /// Larger than the default.
    /// </summary>
    Large
}

