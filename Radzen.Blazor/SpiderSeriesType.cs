namespace Radzen.Blazor
{
    /// <summary>
    /// Specifies the rendering style for a spider chart series.
    /// </summary>
    public enum SpiderSeriesType
    {
        /// <summary>
        /// Renders the series as a filled polygon area (default).
        /// </summary>
        Area,

        /// <summary>
        /// Renders the series as radial column bars extending from the center.
        /// </summary>
        Column
    }
}
