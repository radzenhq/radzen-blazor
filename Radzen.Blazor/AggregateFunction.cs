namespace Radzen;

/// <summary>
/// Specifies the aggregate function for pivot values.
/// </summary>
public enum AggregateFunction
{
    /// <summary>
    /// Sum of values.
    /// </summary>
    Sum,

    /// <summary>
    /// Count of items.
    /// </summary>
    Count,

    /// <summary>
    /// Average of values.
    /// </summary>
    Average,

    /// <summary>
    /// Minimum value.
    /// </summary>
    Min,

    /// <summary>
    /// Maximum value.
    /// </summary>
    Max,

    /// <summary>
    /// First value.
    /// </summary>
    First,

    /// <summary>
    /// Last value.
    /// </summary>
    Last
}

