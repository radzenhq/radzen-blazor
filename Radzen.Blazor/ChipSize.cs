namespace Radzen;

/// <summary>
/// Specifies the size of a <see cref="Radzen.Blazor.RadzenChip" />.
/// </summary>
public enum ChipSize
{
    /// <summary>
    /// The default chip size.
    /// </summary>
    Medium,

    /// <summary>
    /// A chip smaller than the default.
    /// </summary>
    Small,

    /// <summary>
    /// The smallest chip size.
    /// </summary>
    ExtraSmall
}
